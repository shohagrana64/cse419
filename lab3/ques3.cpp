#include <bits/stdc++.h>
#include <string>
using namespace std;
 
int main()
{
	int arr [5][5];
	int m;
	int p;
	int q;
	int sum;
for(int i=0; i<5;i++){
	for(int j=0;j<5;j++){
		cin>>m;
		if(m==1){
			p=i;
			q=j;
		}
	}
}
	sum= abs(p-2)+abs(q-2);
	cout<<sum;
	return 0;
}
